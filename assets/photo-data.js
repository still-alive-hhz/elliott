window.LOCATION_CONFIG = {
  levels: ["country", "province", "city", "district"],
  labels: {
    country: "国家",
    province: "省份",
    city: "城市",
    district: "区县"
  }
};

window.getPhotoLocation = function (photo) {
  if (photo.location) return photo.location;
  var loc = {};
  if (photo.country) loc.country = photo.country;
  if (photo.province) loc.province = photo.province;
  if (photo.city) loc.city = photo.city;
  if (photo.district) loc.district = photo.district;
  return loc;
};

window.getLocationDisplay = function (photo, sep) {
  var loc = window.getPhotoLocation(photo);
  var levels = window.LOCATION_CONFIG.levels;
  var parts = [];
  for (var i = 0; i < levels.length; i++) {
    var key = levels[i];
    if (loc[key]) parts.push(loc[key]);
  }
  return parts.join(sep || " · ");
};

